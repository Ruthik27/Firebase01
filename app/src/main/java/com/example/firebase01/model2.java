package com.example.firebase01;

public class model2 {

    String title,aa, ab, ac;

    public model2() {
    }

    public model2(String title, String aa, String ab, String ac) {
        this.title = title;
        this.aa = aa;
        this.ab = ab;
        this.ac = ac;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAa() {
        return aa;
    }

    public void setAa(String aa) {
        this.aa = aa;
    }

    public String getAb() {
        return ab;
    }

    public void setAb(String ab) {
        this.ab = ab;
    }

    public String getAc() {
        return ac;
    }

    public void setAc(String ac) {
        this.ac = ac;
    }
}


